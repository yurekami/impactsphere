const LOCATION_INDEX: GeoSeed[] = [
  ...COUNTRY_LOCATIONS,
  ...CITY_LOCATIONS,
  ...CLOUD_REGION_LOCATIONS,
  ...TECH_HUB_LOCATIONS,
].sort((left, right) => right.key.length - left.key.length)

function escapeRegex(pattern: string): string {
  return pattern.replace(/[.*+?^${}()|[\]\\]/g, '\\$&')
}

export function geocodeText(text: string): GeoLocation | null {
  const normalized = text.toLowerCase()

  for (const candidate of LOCATION_INDEX) {
    const regex = new RegExp(`\\b${escapeRegex(candidate.key)}\\b`, 'i')
    if (regex.test(normalized)) {
      return candidate.location
    }
  }

  return null
}

export function geocodeEventText(title: string, description: string): GeoLocation | null {
  return geocodeText(`${title} ${description}`)
}

